	/**
	 * converts unified-code to html
	 *
	 * @param	string		$text: text with unified codes like \xf0\x9f\x94\xb8
	 * @param	string		$scriptPath: translation script path
	 * @param	string		$lancode: Language code 'de', 'en'
	 * @param	boolean		$convertcss: converts the span class if TRUE (default)
	 * @return	string		converted text like <span class=\x22moji moji1f538\x22></span>
	 */
	public function emoji_unified_to_html($text, $scriptPath, $lancode='', $convertcss = TRUE){
		$map='unified_to_html';
		if ($lancode !='de' ) {
			if (isset($this->emoijarr['emoji_maps'][$map . 'orig'])) {
				$this->emoijarr['emoji_maps'][$map]=$this->emoijarr['emoji_maps'][$map . 'orig'];
				unset($this->emoijarr['emoji_maps'][$map . 'orig']);
				$this->libemojilnlan =$lancode;
			}

		} else {
			// german is ok
			// need to translate the titles
			if ((!(isset($this->libemojiln))) || ($this->libemojilnlan !=$lancode)){
				if (isset($_SESSION['emoji_maps']['unified_to_html'][$lancode])) {
					$this->emoijarr['emoji_maps']['unified_to_html']=unserialize($_SESSION['emoji_maps']['unified_to_html'][$lancode]);
				} else {
					require_once ($scriptPath);
					$this->libemojiln = new toctoc_comments_emoji_language;
					$this->libemojilnlan =$lancode;
					$spanKeysArray=array_keys($this->emoijarr['emoji_maps'][$map]);
					$this->emoijarr['emoji_maps'][$map . 'orig']=$this->emoijarr['emoji_maps'][$map];
					foreach ($this->emoijarr['emoji_maps'][$map] as $valspanHTML=>$value) {
						$spanHTMLwrk=str_replace('<span title="', '', $value);
						$spanCatTextenglArr=explode('" class=', $spanHTMLwrk);
						$spanCatTextengl=$spanCatTextenglArr[0];  //Small Orange Diamond
						$spanCatRest=$spanCatTextenglArr[1];
						$spanCatKEYengl=$this->emoijarr['categories_to_textcodes'][$spanCatTextengl];  //SMALL ORANGE DIAMOND
						$spanCatTextenglArr[0]=$this->libemojiln->emoji_translate($spanCatKEYengl, $lancode, FALSE);

						if ((mb_detect_encoding($spanCatTextenglArr[0], 'UTF-8', TRUE) === FALSE) == FALSE) {
							$countrogs= count(explode('?', $spanCatTextenglArr[0]));
							$titletmp = utf8_decode($spanCatTextenglArr[0]);
							if (count(explode('?', $titletmp)) <= $countrogs) {
								$spanCatTextenglArr[0] = $titletmp;
							}

						}

						if (mb_detect_encoding($spanCatTextenglArr[0], 'UTF-8', TRUE) === FALSE) {
							$spanCatTextenglArr[0] = utf8_encode($spanCatTextenglArr[0]);
						}

						$spanCatTextenglArr[0] = htmlspecialchars_decode($spanCatTextenglArr[0], ENT_QUOTES);
						$spanCatTextenglArr[0] = html_entity_decode($spanCatTextenglArr[0], ENT_NOQUOTES, 'UTF-8');
						$spanCatTextenglArr[0] = htmlspecialchars($spanCatTextenglArr[0]);
						$spanHTML = '<span title="'.$spanCatTextenglArr[0] .'" class='. $spanCatRest;
						$this->emoijarr['emoji_maps']['unified_to_html']['' . $valspanHTML] = $spanHTML;
						$_SESSION['emoji_maps']['unified_to_html'][$lancode] = serialize($this->emoijarr['emoji_maps']['unified_to_html']);
					}

				}

			}

		}

		$tmptext = $this->emoji_convert($text, 'unified_to_html');
		if ($convertcss) {
			$tmptext=str_replace('moji moji', 'emoji emoji', $tmptext);
		}

		return $tmptext;
	}

	/**
	 * for the emoji-selector, converts :cat: to the html
	 *
	 * @param	string		$text: Key texts like :COOKING:
	 * @param	string		$scriptPath: Path to script with translated emoji-texts
	 * @param	string		$smltexts: Category texts :Cooking:
	 * @param	string		$lancode: Language code 'de', 'en'
	 * @return	string		$textout: Emoji HTML
	 */
	public function emoji_text_to_html($text, $scriptPath, $smltexts = '', $lancode='') {
		$debug=0;
		$textout='';

		$emoijarrcodes = array_flip($this->emoijarr['emoji_maps']['names']);
		$text=substr(trim($text), 1, (strlen(trim($text))-2));

		$textarr=explode(': :', $text);
		$smltextsarr=explode(': :', $smltexts);

		$missingones='';
		$m=0;
		$counttextarr=count($textarr);
		for ($i=0; $i<$counttextarr; $i++) {
			$codeemoji='';
			// get the code
			$codeemoji=$emoijarrcodes[strtoupper($textarr[$i])];
			if ($codeemoji!='') {
				$spanstageone = $this->emoji_unified_to_html($codeemoji, $scriptPath, $lancode, FALSE);
				// now add the u2345-u2342
				$jsontxt=json_encode($codeemoji);
				$outtext= str_replace('\u', '-u', substr($jsontxt, 2, strlen($jsontxt)-3));
				if (substr($outtext, 0, 2) == '-u') {
					$outtext='u'. substr($jsontxt, 1, 1) . $outtext;
				}
				$spanstageone= str_replace('"></span', ' ' . $outtext .  '"></span', $spanstageone);
				$spanstageone= str_replace('</span>', '</span></span>', $spanstageone);
				$spanstageone= str_replace('<span', '<span  class="tx-tc-emo-int-img tx-tc-margin0" id="tx-tc-emo-int-img-' . $outtext . '"><span', $spanstageone);

				$textout .=  $spanstageone . ' ';
			} else {
				$missingones .=  ':' . $textarr[$i] . ': ';
				$m++;
			}

		}

		if ($debug==1) {
			print '<br>Missing: ' . $m . '<br>' . $missingones . '<br>';
		}

		return $textout;
	}

