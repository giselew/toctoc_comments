	/**
	 * converts english-key to lowercase in a given language, category or text
	 *
	 * @param	string		$text:
	 * @param	string		$lancode:
	 * @param	boolean		$iscat: if true then return text from categorylist
	 * @return	string		converted text like ...
	 */
	public function emoji_translate($text, $lancode, $iscat){
		if ($iscat) {
			$res=$this->emoijcategorylan[$lancode][$text];
		} else {
			$res=$this->emoijtextlan[$lancode][$text];
		}

		return $res;
	}
